#ifndef blu_token_type_h
#define blu_token_type_h

typedef enum {
	// Single-character tokens.
	TOKEN_AT,
	TOKEN_COLON,
	TOKEN_COMMA,
	TOKEN_DOT,
	TOKEN_LEFT_BRACE,
	TOKEN_LEFT_BRACKET,
	TOKEN_LEFT_PAREN,
	TOKEN_RIGHT_BRACE,
	TOKEN_RIGHT_BRACKET,
	TOKEN_RIGHT_PAREN,
	TOKEN_SEMICOLON,

	// One or two character tokens.
	TOKEN_BANG_EQUAL,
	TOKEN_BANG,
	TOKEN_EQUAL_EQUAL,
	TOKEN_EQUAL,
	TOKEN_GREATER_EQUAL,
	TOKEN_GREATER,
	TOKEN_LESS_EQUAL,
	TOKEN_LESS,
	TOKEN_MINUS,
	TOKEN_PERCENT,
	TOKEN_PLUS,
	TOKEN_SLASH,
	TOKEN_STAR,

	// Literals.
	TOKEN_IDENTIFIER,
	TOKEN_NUMBER,
	TOKEN_STRING,

	// Keywords.
	TOKEN_AND,
	TOKEN_ASSERT,
	TOKEN_BREAK,
	TOKEN_CLASS,
	TOKEN_ELSE,
	TOKEN_FALSE,
	TOKEN_FN,
	TOKEN_FOR,
	TOKEN_IF,
	TOKEN_NIL,
	TOKEN_OR,
	TOKEN_RETURN,
	TOKEN_SUPER,
	TOKEN_TRUE,
	TOKEN_VAR,
	TOKEN_WHILE,

	TOKEN_EOF,
	TOKEN_NEWLINE,

	TOKEN_ERROR,
} bluTokenType;

#endif
